import json
import os
from http.server import BaseHTTPRequestHandler

BASE_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
with open(os.path.join(BASE_DIR, "channels.json")) as f:
    CHANNELS = json.load(f)


class handler(BaseHTTPRequestHandler):
    def do_GET(self):
        path = self.path.lstrip("/").split("?")[0]
        parts = path.split("/")

        # Expect: /stream-dash/{slug}/master.mpd
        if len(parts) != 3 or parts[0] != "stream-dash" or parts[2] != "master.mpd":
            self._error(400, "Bad request: expected /stream-dash/{channel}/master.mpd")
            return

        slug = parts[1]

        if slug not in CHANNELS:
            self._error(404, f"Channel not found: {slug}")
            return

        origin_url = CHANNELS[slug]
        headers_meta = CHANNEL_HEADERS.get(slug, {})

        self.send_response(301)
        self.send_header("Location", origin_url)
        self.send_header("Access-Control-Allow-Origin", "*")
        self.send_header("Cache-Control", "no-cache")

        # Hardcoded IPTV headers per channel
        if headers_meta.get("extvlcopt"):
            self.send_header("X-EXTVLCOPT", headers_meta["extvlcopt"])
        if headers_meta.get("kodiprop1"):
            self.send_header("X-KODIPROP-1", headers_meta["kodiprop1"])
        if headers_meta.get("kodiprop2"):
            self.send_header("X-KODIPROP-2", headers_meta["kodiprop2"])

        self.end_headers()

    def do_OPTIONS(self):
        self.send_response(204)
        self.send_header("Access-Control-Allow-Origin", "*")
        self.send_header("Access-Control-Allow-Methods", "GET, OPTIONS")
        self.end_headers()

    def _error(self, code, msg):
        self.send_response(code)
        self.send_header("Content-Type", "text/plain")
        self.send_header("Access-Control-Allow-Origin", "*")
        self.end_headers()
        self.wfile.write(msg.encode())

    def log_message(self, format, *args):
        pass
